-- ============================================================================
-- 6.1 — B2B SaaS sample schema (the honest benchmark's target, rebuilt).
--
-- A realistic product database (organizations, plans, users, subscriptions,
-- usage_events, invoices, payments) that is STRUCTURALLY DIFFERENT from Chinook:
--
--   * lowercase snake_case identifiers (Chinook uses quoted PascalCase) — this
--     re-proves NIXUS's introspection is identifier-agnostic.
--   * named PostgreSQL enum types (exercises 2.2 enum capture).
--   * numeric(10,2) money columns, timestamps, booleans (type fidelity).
--   * a real FK graph with a MULTI-HOP chain (payments -> invoices ->
--     organizations -> plans) and a NULLABLE FK (usage_events.user_id) so
--     LEFT-join reasoning has something to chew on.
--   * a couple of table/column COMMENTS (exercises comment capture).
--
-- Loaded by scripts/rebuild_saas_db.py through the writable OWNER connection,
-- exactly like Chinook is loaded by scripts/migrate_chinook.py. The running app
-- only ever sees this database through the read-only nixus_readonly role.
--
-- This file is idempotent-friendly: rebuild_saas_db.py DROPs and recreates the
-- whole database before running it, so it always starts from a clean slate.
-- ============================================================================

-- ── Named enum types (2.2 enum capture) ─────────────────────────────────────
CREATE TYPE plan_tier AS ENUM ('free', 'pro', 'enterprise');
CREATE TYPE user_role AS ENUM ('admin', 'member', 'viewer');
CREATE TYPE subscription_status AS ENUM ('active', 'trialing', 'canceled', 'past_due');
CREATE TYPE invoice_status AS ENUM ('paid', 'open', 'void');

-- ── plans ───────────────────────────────────────────────────────────────────
CREATE TABLE plans (
    id            integer       PRIMARY KEY,
    name          text          NOT NULL,
    tier          plan_tier     NOT NULL,
    monthly_price numeric(10, 2) NOT NULL,
    seat_limit    integer
);

-- ── organizations (the tenants; FK -> plans) ────────────────────────────────
CREATE TABLE organizations (
    id         integer   PRIMARY KEY,
    name       text      NOT NULL,
    plan_id    integer   REFERENCES plans (id),
    created_at timestamp NOT NULL,
    country    text,
    is_active  boolean   NOT NULL DEFAULT true
);

-- ── users (FK -> organizations; last_active_at nullable) ─────────────────────
CREATE TABLE users (
    id              integer   GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    organization_id integer   NOT NULL REFERENCES organizations (id),
    email           text      NOT NULL,
    full_name       text,
    role            user_role NOT NULL,
    created_at      timestamp NOT NULL,
    last_active_at  timestamp            -- nullable: some users never returned
);

-- ── subscriptions (FK -> organizations, plans; canceled_at nullable) ────────
CREATE TABLE subscriptions (
    id              integer             PRIMARY KEY,
    organization_id integer             REFERENCES organizations (id),
    plan_id         integer             REFERENCES plans (id),
    status          subscription_status NOT NULL,
    started_at      timestamp           NOT NULL,
    canceled_at     timestamp,          -- set only for status = 'canceled'
    seats           integer
);

-- ── usage_events (FK -> organizations; user_id is a NULLABLE FK -> users) ────
-- user_id is intentionally nullable (org-level / system events have no user)
-- AND independent of organization_id — it is a valid users.id or NULL, not
-- constrained to the same org. This keeps the "orgs with zero usage" answer
-- determinate (events are bucketed by organization_id only).
CREATE TABLE usage_events (
    id              integer   GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    organization_id integer   NOT NULL REFERENCES organizations (id),
    user_id         integer   REFERENCES users (id),
    event_type      text      NOT NULL,
    occurred_at     timestamp NOT NULL,
    quantity        integer   NOT NULL DEFAULT 1
);

-- ── invoices (FK -> organizations) ──────────────────────────────────────────
CREATE TABLE invoices (
    id              integer        GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    organization_id integer        NOT NULL REFERENCES organizations (id),
    amount          numeric(10, 2) NOT NULL,
    status          invoice_status NOT NULL,
    issued_at       timestamp      NOT NULL,
    due_at          timestamp      NOT NULL
);

-- ── payments (FK -> invoices — completes the multi-hop chain to organizations)
CREATE TABLE payments (
    id         integer        GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    invoice_id integer        NOT NULL REFERENCES invoices (id),
    amount     numeric(10, 2) NOT NULL,
    paid_at    timestamp      NOT NULL,
    method     text
);

-- ── Comments (2.2 comment capture) ──────────────────────────────────────────
COMMENT ON TABLE organizations IS 'Customer accounts (tenants) on the SaaS platform.';
COMMENT ON TABLE usage_events IS 'Per-event product usage telemetry; user_id is null for org-level/system events.';
COMMENT ON COLUMN invoices.amount IS 'Invoice total in USD.';
COMMENT ON COLUMN organizations.country IS 'ISO 3166-1 alpha-2 country code of the billing address.';
